const MESSAGES: Record<Locale, Record<MessageKey, string>> = { vi, en };

export type TranslateParams = Record<string, string | number>;
export type TranslateFn = (key: MessageKey, params?: TranslateParams) => string;

/** Resolve a message for a locale, interpolating `{name}` placeholders. */
export function translate(locale: Locale, key: MessageKey, params?: TranslateParams): string {
  const dict = MESSAGES[locale] ?? MESSAGES[DEFAULT_LOCALE];
  let text = dict[key] ?? MESSAGES[DEFAULT_LOCALE][key] ?? key;
  if (params) {
    for (const [name, value] of Object.entries(params)) {
      text = text.replaceAll(`{${name}}`, String(value));
    }
  }
  return text;
}
